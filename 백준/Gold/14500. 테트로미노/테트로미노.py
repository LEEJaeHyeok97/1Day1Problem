N, M = map(int, input().split())

arr = [list(map(int, input().split())) for _ in range(N)]


tets = [[(0, 1), (0, 2), (0, 3)], [(1, 0), (2, 0), (3, 0)],
        [(0, 1), (1, 1), (1, 0)],
        [(1, 0), (2, 0), (2, 1)], [(0, 1), (0, 2), (-1, 2)], [(0, 1), (1, 1), (2, 1)], [(1, 0), (0, 1), (0, 2)],
        [(0, 1), (-1, 1), (-2, 1)], [(1, 0), (1, 1), (1, 2)], [(0, -1), (1, -1), (2, -1)], [(0, 1), (0, 2), (1, 2)],
        [(1, 0), (1, 1), (2, 1)], [(0, 1), (-1, 1), (-1, 2)], [(-1, 0), (-1, 1), (-2, 1)], [(0, 1), (1, 1), (1, 2)],
        [(0, 1), (0, 2), (1, 1)], [(0, 1), (-1, 1), (1, 1)], [(0, 1), (-1, 1), (0, 2)], [(0, 1), (-1, 0), (1, 0)]]



def f(i, j, k):
    tmp = arr[i][j]

    for d in k:
        if (i + int(d[0])) < 0 or (i + int(d[0]) >= N) or (j + int(d[1]) < 0) or (j + int(d[1]) >= M):
            return 0
        tmp += arr[i + d[0]][j + d[1]]
    return tmp  

ans = 0
for i in range(N):
    for j in range(M):
        for k in tets:
            ans = max(f(i, j, k), ans)

print(ans)